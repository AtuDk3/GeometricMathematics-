package model;

/**
 *
 * @author Lenovo
 */
public class Point {
    private double x, y;

    public Point() {
        this.x = 0;
        this.y = 0;
    }

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setXY(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public boolean isSame(Point d) {
        return this.x == d.getX() && this.y == d.getY();
    }

    public void printXY() {
        System.out.printf("(%.2f, %.2f)", x, y);
    }
}

